package com.example.carlosmc.facultades;


public class Facus {

    private String name;
    private int idDrawable;

    public Facus(String name, int idDrawable) {
        this.name = name;
        this.idDrawable = idDrawable;
    }

    public String getName() {
        return name;
    }

    public int getIdDrawable() {
        return idDrawable;
    }
}
